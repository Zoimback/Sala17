package com.spania.sala17;

public class Entrada
{
    String descipción;
    int img;

    public Entrada(String descipción, int img)
    {
        this.descipción = descipción;
        this.img = img;
    }

    public String getDescipción()
    {
        return descipción;
    }

    public int getImg() {
        return img;
    }
}
